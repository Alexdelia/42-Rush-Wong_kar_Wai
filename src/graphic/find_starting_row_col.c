#include "../../inc/wkw.h"

void	find_starting_row_col(t_env *e, size_t *row, size_t *col)
{
	*row = (e->row - (e->size * 5 + 1)) / 2;
	*col = (e->col - (e->size * 12 + 1)) / 2;
}
